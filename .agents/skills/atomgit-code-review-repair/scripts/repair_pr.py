#!/usr/bin/env python3
"""
AtomGit PR Review Comment Repair Script
自动修复 AtomGit PR 检视意见

用法：
    python3 repair_pr.py --pr-url <pr_url> [--dry-run] [--work-dir .]
"""

import os
import sys
import json
import argparse
import subprocess
from pathlib import Path
from typing import List, Dict, Optional

sys.path.insert(0, str(Path(__file__).parent.parent / "lib"))

from atomgit_api_repair import AtomGitAPIRepair, FixResult


def load_config(config_path: str) -> dict:
    """加载配置"""
    with open(config_path, "r", encoding="utf-8") as f:
        config = json.load(f)

    if not config.get("atomgit") or not config["atomgit"].get("token"):
        raise Exception("配置文件中缺少 atomgit.token")

    return config


def format_fix_reply(fix: FixResult, comment: dict) -> str:
    """格式化修复回复"""
    return f"""感谢您的审阅意见。已按要求完成修复，具体内容如下：

**修复方案**:
{fix.fix_description}

**修改文件**: `{comment.get("path", "unknown")}`

**代码变更**:
```
# 修改前 (Before)
{fix.original_code}

# 修改后 (After)
{fix.fixed_code}
```

如有任何疑问，请随时提出。

---
🤖 Generated by atomgit-code-review-repair"""


def format_logic_reply(fix: FixResult, comment: dict) -> str:
    """格式化逻辑回复"""
    return f"""感谢您的审阅意见。

{fix.reason}

如有任何疑问，请随时提出。

---
🤖 Generated by atomgit-code-review-repair"""


def format_revert_reply(fix: FixResult, comment: dict) -> str:
    """格式化回退回复"""
    return f"""感谢您的审阅意见。

**处理结果**:
{fix.fix_description}

已按要求回退该文件的修改，恢复到原仓库状态。

如有任何疑问，请随时提出。

---
🤖 Generated by atomgit-code-review-repair"""


def format_delete_lines_reply(fix: FixResult, comment: dict) -> str:
    """格式化删除行回复"""
    lines_str = ", ".join(map(str, fix.delete_lines))
    return f"""感谢您的审阅意见。

**处理结果**:
{fix.fix_description}

已删除以下行号的内容: {lines_str}

如有任何疑问，请随时提出。

---
🤖 Generated by atomgit-code-review-repair"""


def apply_fix(file_path: str, original_code: str, fixed_code: str) -> None:
    """应用代码修复"""
    with open(file_path, "r", encoding="utf-8") as f:
        content = f.read()

    new_content = content.replace(original_code, fixed_code)

    if new_content == content:
        raise Exception("无法找到原始代码。代码可能已更改。")

    with open(file_path, "w", encoding="utf-8") as f:
        f.write(new_content)

    print(f"  ✓ 已应用修复到 {file_path}")


def delete_specific_lines(file_path: str, line_numbers: List[int]) -> None:
    """删除特定行"""
    with open(file_path, "r", encoding="utf-8") as f:
        content = f.read()

    lines = content.split("\n")
    indices_to_delete = sorted(set(n - 1 for n in line_numbers), reverse=True)
    deleted_count = 0

    for index in indices_to_delete:
        if 0 <= index < len(lines):
            lines.pop(index)
            deleted_count += 1
        else:
            print(f"  ⚠ 行号 {index + 1} 超出范围 (文件有 {len(lines)} 行)")

    new_lines = "\n".join(lines)

    with open(file_path, "w", encoding="utf-8") as f:
        f.write(new_lines)

    print(f"  ✓ 已删除 {deleted_count} 行从 {file_path}")


def revert_file_changes(file_path: str, work_dir: str) -> bool:
    """回退文件修改"""
    try:
        relative_path = os.path.relpath(file_path, work_dir)

        result = subprocess.run(
            ["git", "checkout", "HEAD", "--", relative_path],
            cwd=work_dir,
            capture_output=True,
            text=True,
        )

        if result.returncode == 0:
            print(f"  ✓ 已回退 {relative_path} 到基线状态")
            return True
        else:
            print(f"  ⚠ 回退失败: {result.stderr}")
            return False
    except Exception as e:
        print(f"  ✗ 回退失败: {e}")
        return False


def commit_fixes(work_dir: str = ".") -> bool:
    """提交修复"""
    result = subprocess.run(
        ["git", "status", "--porcelain"], capture_output=True, text=True, cwd=work_dir
    )

    if not result.stdout.strip():
        print("⚠ 没有需要提交的更改")
        return False

    subprocess.run(["git", "add", "-A"], capture_output=True, cwd=work_dir)
    print("✓ 已暂存更改")

    subprocess.run(
        ["git", "commit", "--amend", "--no-edit"], capture_output=True, cwd=work_dir
    )
    print("✓ 已将修复追加到提交")

    return True


def print_summary(results: List[dict], pr_url: str) -> None:
    """打印修复汇总"""
    print("\n" + "=" * 80)
    print("📋 修复汇总 / Fix Summary")
    print("=" * 80)

    if not results:
        print("\n✓ 所有检视意见已处理")
        print("=" * 80)
        return

    print("\n| 状态 | 文件 | 检视意见 | 处理结果 |")
    print("|------|------|----------|----------|")

    for result in results:
        status = "✅ 已修复" if result.get("has_fix") else "💬 已回复"
        file_name = result.get("file", "").split("/")[-1]
        comment = (
            (result.get("comment_body", "")[:20] + "...")
            if result.get("comment_body")
            else "-"
        )
        fix = result.get("fix_description") or result.get("reason") or "-"

        print(f"| {status} | {file_name} | {comment} | {fix[:30]}... |")

    print("\n" + "=" * 80)
    print(f"🔗 PR 链接: {pr_url}")
    print("=" * 80 + "\n")


def main():
    parser = argparse.ArgumentParser(description="AtomGit PR 检视意见修复")
    parser.add_argument("--pr-url", type=str, required=True, help="PR URL")
    parser.add_argument(
        "--config", type=str, default="config.json", help="配置文件路径"
    )
    parser.add_argument("--dry-run", action="store_true", help="仅显示计划，不执行")
    parser.add_argument("--work-dir", type=str, default=".", help="工作目录")
    args = parser.parse_args()

    try:
        config = load_config(args.config)
    except FileNotFoundError:
        print(f"❌ 配置文件不存在: {args.config}")
        sys.exit(1)
    except Exception as e:
        print(f"❌ 加载配置失败: {e}")
        sys.exit(1)

    api = AtomGitAPIRepair.from_config(args.config)

    print("\n" + "=" * 60)
    print("🔧 AtomGit PR 检视意见修复工具")
    print("=" * 60)

    print(f"\n📋 解析 PR 链接...")
    try:
        pr_info = api.parse_pr_url(args.pr_url)
    except Exception as e:
        print(f"❌ {e}")
        sys.exit(1)

    print(f"  Owner: {pr_info['owner']}")
    print(f"  Repo: {pr_info['repo']}")
    print(f"  PR: #{pr_info['pr_number']}")

    print("\n📝 获取未解决检视意见...")
    comments = api.get_unresolved_comments(pr_info["pr_number"])

    if not comments:
        print("\n✅ 没有需要修复的检视意见")
        return

    print(f"  找到 {len(comments)} 条未解决检视意见\n")

    results = []
    files_fixed = []

    for i, comment in enumerate(comments, 1):
        print(
            f"\n[{i}/{len(comments)}] 处理 {comment['path']}:{comment.get('line', 1)}"
        )

        try:
            file_path = os.path.join(args.work_dir, comment["path"])
            line_number = comment.get("line") or 1

            try:
                file_content = api.get_file_context(file_path, line_number, 20)
            except Exception as e:
                print(f"  ⚠ 无法读取文件: {e}")
                continue

            fix = api.generate_fix(
                {
                    "file_content": file_content,
                    "review_comment": comment["body"],
                    "file_path": comment["path"],
                    "line_number": line_number,
                    "pr_diff": "",
                }
            )

            reply_body = ""

            if fix.needs_revert_file:
                print(f"\n⚠️ 需要回退整个文件")
                print(f"  文件: {fix.file_path}")
                print(f"  说明: {fix.fix_description}")

                if not args.dry_run:
                    revert_file_changes(
                        os.path.join(args.work_dir, fix.file_path), args.work_dir
                    )
                    files_fixed.append(fix.file_path)

                reply_body = format_revert_reply(fix, comment)

            elif fix.needs_delete_lines:
                print(f"\n⚠️ 需要删除特定行")
                print(f"  文件: {fix.file_path}")
                print(f"  要删除的行: {fix.delete_lines}")
                print(f"  说明: {fix.fix_description}")

                if not args.dry_run:
                    delete_specific_lines(
                        os.path.join(args.work_dir, fix.file_path), fix.delete_lines
                    )
                    files_fixed.append(fix.file_path)

                reply_body = format_delete_lines_reply(fix, comment)

            elif fix.has_fix:
                print(f"\n🔧 应用代码修复...")
                if not args.dry_run:
                    try:
                        apply_fix(
                            os.path.join(args.work_dir, comment["path"]),
                            fix.original_code,
                            fix.fixed_code,
                        )
                        files_fixed.append(comment["path"])
                    except Exception as e:
                        print(f"  ✗ 应用修复失败: {e}")

                reply_body = format_fix_reply(fix, comment)

            else:
                reply_body = format_logic_reply(fix, comment)

            if not args.dry_run:
                print("\n💬 回复检视意见...")
                reply_result = api.reply_to_comment(
                    pr_info["pr_number"], comment["id"], reply_body
                )
                print(f"  ✓ 已回复评论 #{comment['id']}")

            results.append(
                {
                    "file": comment["path"],
                    "line": comment.get("line"),
                    "comment_body": comment["body"][:50] + "...",
                    "has_fix": fix.has_fix,
                    "fix_description": fix.fix_description
                    if fix.has_fix
                    else fix.reason,
                    "url": api.get_pr_url(pr_info["pr_number"]),
                }
            )

        except Exception as e:
            print(f"  ✗ 处理失败: {e}")
            results.append(
                {
                    "file": comment["path"],
                    "line": comment.get("line"),
                    "comment_body": comment["body"][:50] + "...",
                    "has_fix": False,
                    "error": str(e),
                    "url": comment.get("url"),
                }
            )

    if files_fixed and not args.dry_run:
        print("\n📦 提交修复...")
        try:
            commit_fixes(args.work_dir)
        except Exception as e:
            print(f"  ✗ 提交失败: {e}")

    print_summary(results, api.get_pr_url(pr_info["pr_number"]))

    print("\n✅ 任务完成!\n")


if __name__ == "__main__":
    main()
