"""
AtomGit API Repair Extension
Extends AtomGitAPI with review comment repair functionality
"""

import os
import re
import json
import base64
import subprocess
import requests
from typing import Optional, Dict, List, Any
from dataclasses import dataclass, field
from urllib.parse import quote as url_quote


@dataclass
class AtomGitConfig:
    """AtomGit API 配置"""

    token: str
    owner: str
    repo: str
    base_url: str = "https://api.atomgit.com"


@dataclass
class FixResult:
    """修复结果"""

    has_fix: bool = False
    needs_reply_only: bool = False
    needs_revert_file: bool = False
    needs_delete_lines: bool = False
    file_path: str = ""
    delete_lines: List[int] = field(default_factory=list)
    fix_description: str = ""
    original_code: str = ""
    fixed_code: str = ""
    reason: str = ""


class AtomGitAPIRepair:
    """AtomGit API 修复扩展类"""

    def __init__(
        self,
        config: AtomGitConfig,
        anthropic_api_key: str = "",
        anthropic_base_url: str = "",
    ):
        self.config = config
        self.anthropic_api_key = anthropic_api_key or ""
        self.anthropic_base_url = anthropic_base_url or ""
        self.headers = {
            "Authorization": f"Bearer {config.token}",
            "Content-Type": "application/json",
            "User-Agent": "AtomGit-Review-Repair/1.0",
        }

    def request(
        self, endpoint: str, method: str = "GET", body: Optional[dict] = None
    ) -> Any:
        """发送 HTTP 请求"""
        url = f"{self.config.base_url}{endpoint}"

        response = requests.request(
            method=method, url=url, headers=self.headers, json=body
        )

        if response.status_code not in (200, 201):
            raise Exception(f"API 请求失败: {response.status_code} - {response.text}")

        return response.json()

    def parse_pr_url(self, pr_url: str) -> dict:
        """解析 PR URL"""
        patterns = [
            r"atomgit\.com/([^/]+)/([^/]+)/pulls?/(\d+)",
        ]

        for pattern in patterns:
            match = re.search(pattern, pr_url)
            if match:
                return {
                    "owner": match.group(1),
                    "repo": match.group(2).replace(".git", ""),
                    "pr_number": int(match.group(3)),
                }

        raise Exception(f"无效的 AtomGit PR URL: {pr_url}")

    def get_pr_comments(self, pr_number: int) -> List[dict]:
        """获取 PR 评论"""
        return self.request(
            f"/api/v5/repos/{self.config.owner}/{self.config.repo}/pulls/{pr_number}/comments"
        )

    def get_pr_files(self, pr_number: int) -> List[dict]:
        """获取 PR 文件变更"""
        return self.request(
            f"/api/v5/repos/{self.config.owner}/{self.config.repo}/pulls/{pr_number}/files"
        )

    def get_file_content(self, file_path: str, ref: str = "HEAD") -> str:
        """获取文件内容"""
        encoded_path = url_quote(file_path, safe="")
        data = self.request(
            f"/api/v5/repos/{self.config.owner}/{self.config.repo}/contents/{encoded_path}?ref={ref}"
        )

        if data.get("content"):
            return base64.b64decode(data["content"]).decode("utf-8")
        return ""

    def get_pr_url(self, pr_number: int) -> str:
        """获取 PR URL"""
        return f"https://atomgit.com/{self.config.owner}/{self.config.repo}/pull/{pr_number}"

    def get_unresolved_comments(self, pr_number: int) -> List[dict]:
        """获取未解决的检视意见"""
        comments = self.get_pr_comments(pr_number)

        unresolved = []
        for comment in comments:
            if not comment.get("resolved_at"):
                unresolved.append(
                    {
                        "id": comment.get("id"),
                        "path": comment.get("path") or comment.get("diff_file"),
                        "position": comment.get("position"),
                        "line": comment.get("new_line") or comment.get("old_line"),
                        "body": comment.get("body"),
                        "commit_id": comment.get("commit_id"),
                        "user": comment.get("author", {}).get("username", "Unknown"),
                        "url": f"{self.get_pr_url(pr_number)}#discussion-{comment.get('discussion_id', comment.get('id'))}",
                    }
                )

        return unresolved

    def reply_to_comment(
        self, pr_number: int, comment_id: int, reply_body: str
    ) -> dict:
        """回复检视意见"""
        payload = {
            "body": reply_body
            + f"\n\n_Replying to comment #{comment_id}_\n\n---\n🤖 Generated by atomgit-code-review-repair"
        }

        return self.request(
            f"/api/v5/repos/{self.config.owner}/{self.config.repo}/pulls/{pr_number}/comments",
            method="POST",
            body=payload,
        )

    def get_file_context(
        self, file_path: str, line_number: int, context_lines: int = 20
    ) -> str:
        """获取文件上下文"""
        try:
            content = self.get_file_content(file_path)
            lines = content.split("\n")

            start = max(0, line_number - context_lines - 1)
            end = min(len(lines), line_number + context_lines)

            return "\n".join(lines[start:end])
        except Exception as e:
            raise Exception(f"无法读取文件 {file_path}: {e}")

    def generate_fix(self, context: dict) -> FixResult:
        """使用 LLM 生成修复方案"""
        if not self.anthropic_api_key:
            return FixResult(
                has_fix=False,
                needs_reply_only=True,
                reason="感谢您的检视意见。由于自动化修复服务暂时不可用，请您手动处理此问题。如有疑问，请随时提出。",
            )

        prompt = self._build_fix_prompt(context)

        try:
            response_text = self._call_llm(prompt)
            return self._parse_fix_response(response_text, context.get("file_path", ""))
        except Exception as e:
            print(f"  ⚠ LLM 调用失败: {e}")
            return FixResult(
                has_fix=False,
                needs_reply_only=True,
                reason="感谢您的检视意见。LLM 服务暂时不可用，请手动处理。",
            )

    def _build_fix_prompt(self, context: dict) -> str:
        """构建 LLM prompt"""
        file_content = context.get("file_content", "")
        review_comment = context.get("review_comment", "")
        file_path = context.get("file_path", "")
        line_number = context.get("line_number", 0)
        pr_diff = context.get("pr_diff", "")

        return f"""你是一个代码修复助手。收到了一条代码审查意见。

**文件**: {file_path}
**行号**: {line_number}

**审查意见**:
{review_comment}

**当前PR中的文件改动**:
```
{pr_diff or "(无法获取改动信息)"}
```

**当前代码内容** (第{line_number}行附近):
```
{file_content}
```

请使用以下格式之一回答：

**格式A - 需要删除特定行**:
```
HAS_FIX: true
NEEDS_REVERT_FILE: false
NEEDS_DELETE_LINES: true
DELETE_LINES: [要删除的行号列表，例如: 18,30,45-179]
FIX_DESCRIPTION: [删除原因]
FILE_PATH: {file_path}
```

**格式B - 需要回退整个文件**:
```
HAS_FIX: true
NEEDS_REVERT_FILE: true
FIX_DESCRIPTION: [回退原因]
FILE_PATH: {file_path}
```

**格式C - 需要代码修复**:
```
HAS_FIX: true
NEEDS_REVERT_FILE: false
FIX_DESCRIPTION: [修复方案描述]
ORIGINAL_CODE: [原代码]
FIXED_CODE: [修复后代码]
```

**格式D - 仅需回复说明**:
```
HAS_FIX: false
NEEDS_REPLY_ONLY: true
REASON: [说明内容]
```

请简明扼要地回答。"""

    def _call_llm(self, prompt: str) -> str:
        """调用 LLM API"""
        import anthropic

        client_options = {"api_key": self.anthropic_api_key}
        if self.anthropic_base_url:
            client_options["base_url"] = self.anthropic_base_url

        client = anthropic.Anthropic(**client_options)

        response = client.messages.create(
            model="claude-sonnet-4-20250514",
            max_tokens=2000,
            messages=[{"role": "user", "content": prompt}],
        )

        return response.content[0].text

    def _parse_fix_response(self, response_text: str, file_path: str) -> FixResult:
        """解析 LLM 响应"""
        result = FixResult(file_path=file_path)

        lines = response_text.split("\n")
        current_section = None

        for line in lines:
            if "HAS_FIX:" in line:
                result.has_fix = "true" in line.lower()
            elif "NEEDS_REPLY_ONLY:" in line:
                result.needs_reply_only = "true" in line.lower()
            elif "NEEDS_REVERT_FILE:" in line:
                result.needs_revert_file = "true" in line.lower()
            elif "NEEDS_DELETE_LINES:" in line:
                result.needs_delete_lines = "true" in line.lower()
            elif "DELETE_LINES:" in line:
                lines_str = line.replace("DELETE_LINES:", "").strip()
                result.delete_lines = self._parse_delete_lines(lines_str)
            elif "FILE_PATH:" in line:
                result.file_path = line.replace("FILE_PATH:", "").strip()
            elif "FIX_DESCRIPTION:" in line:
                result.fix_description = line.replace("FIX_DESCRIPTION:", "").strip()
                current_section = "fix_description"
            elif "ORIGINAL_CODE:" in line:
                current_section = "original_code"
            elif "FIXED_CODE:" in line:
                current_section = "fixed_code"
            elif "REASON:" in line:
                result.reason = line.replace("REASON:", "").strip()
                current_section = "reason"
            elif current_section:
                if current_section == "fix_description":
                    result.fix_description += line
                elif current_section == "original_code":
                    result.original_code += line + "\n"
                elif current_section == "fixed_code":
                    result.fixed_code += line + "\n"
                elif current_section == "reason":
                    result.reason += line + "\n"

        result.original_code = result.original_code.strip()
        result.fixed_code = result.fixed_code.strip()
        result.reason = result.reason.strip()

        return result

    def _parse_delete_lines(self, lines_str: str) -> List[int]:
        """解析删除行号"""
        lines = []
        parts = [p.strip() for p in lines_str.strip("[]").split(",")]

        for part in parts:
            if "-" in part and not part.startswith("-"):
                range_parts = part.split("-")
                if len(range_parts) == 2:
                    try:
                        start = int(range_parts[0].strip())
                        end = int(range_parts[1].strip())
                        lines.extend(range(start, end + 1))
                    except ValueError:
                        pass
            else:
                try:
                    lines.append(int(part))
                except ValueError:
                    pass

        return sorted(set(lines))

    @staticmethod
    def from_config(config_path: str = "config.json") -> "AtomGitAPIRepair":
        """从配置文件创建实例"""
        with open(config_path, "r", encoding="utf-8") as f:
            config = json.load(f)

        atomgit_config = AtomGitConfig(
            token=config["atomgit"]["token"],
            owner=config["atomgit"]["owner"],
            repo=config["atomgit"]["repo"],
            base_url=config["atomgit"].get("baseUrl", "https://api.atomgit.com"),
        )

        anthropic_api_key = config.get("anthropic", {}).get("apiKey", "")
        anthropic_base_url = config.get("anthropic", {}).get("baseUrl", "")

        return AtomGitAPIRepair(atomgit_config, anthropic_api_key, anthropic_base_url)
